package ca.ulaval.glo4003.ULavalWebTimeSheet.Web.ViewModels;

public class CreerEmployeForm
{

    public String adresseCourriel;
    public String motDePasse;
    public String role;
    public String prenom;
    public String nom;
    public String adresseResidence;
    public String numTelephone;

    public String getAdresseCourriel() {
	return adresseCourriel;
    }

    public void setAdresseCourriel(String adresseCourriel) {
	this.adresseCourriel = adresseCourriel;
    }

    public String getMotDePasse() {
	return motDePasse;
    }

    public void setMotDePasse(String motDePasse) {
	this.motDePasse = motDePasse;
    }

    public String getRole() {
	return role;
    }

    public void setRole(String role) {
	this.role = role;
    }

    public String getPrenom() {
	return prenom;
    }

    public void setPrenom(String prenom) {
	this.prenom = prenom;
    }

    public String getNom() {
	return nom;
    }

    public void setNom(String nom) {
	this.nom = nom;
    }

    public String getAdresseResidence() {
	return adresseResidence;
    }

    public void setAdresseResidence(String adresseResidence) {
	this.adresseResidence = adresseResidence;
    }

    public String getNumTelephone() {
	return numTelephone;
    }

    public void setNumTelephone(String numTelephone) {
	this.numTelephone = numTelephone;
    }

}
